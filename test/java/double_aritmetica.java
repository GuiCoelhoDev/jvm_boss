public class double_aritmetica {
  public static void main(String[] paramArrayOfString) {
    double d1 = 98.24D;
    double d2 = 134.87D;
    System.out.println(d1 + d2);
    System.out.println(d1 - d2);
    System.out.println(d1 * d2);
    System.out.println(d1 / d2);
    System.out.println(-d1);
    System.out.println(d2 % d1);
  }
}
