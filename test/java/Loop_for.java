class Loop_for {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++ ) {
            System.out.println(i);
        }
        
        System.out.println();

        int[] vetor_int = {1,2,3,4,5};

        for(int n:vetor_int) {
            System.out.println(n);
        }
    }
}
