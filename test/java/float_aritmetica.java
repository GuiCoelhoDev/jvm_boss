public class float_aritmetica {
  public static void main(String[] paramArrayOfString) {
    float f1 = 100.4F;
    float f2 = 20.3F;
    System.out.println(f1 + f2);
    System.out.println(f1 - f2);
    System.out.println(f1 * f2);
    System.out.println(f1 / f2);
    System.out.println(-f1);
    System.out.println(f1 % f2);
  }
}